    private void jButtonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListoActionPerformed
        
            
        if(jTextFieldFecha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó la fecha del viaje");
            
        }
        else if(jTextFieldPrecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó el precio del viaje");
        }else if(jTextFieldDescripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó la descripción del viaje");
        }
        
        else{
            
            if(jComboBoxPago.getSelectedItem().toString().equals("SI")){
                try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("INSERT INTO viajes VALUES('0' , "
                        + "'"+jTextFieldFecha.getText()+"' , "
                        + "'"+jTextFieldPrecio.getText()+"' , "
                        + "'"+jComboBoxCliente.getSelectedItem().toString()+"' ,"
                        + "'"+jTextFieldDescripcion.getText()+"')");
                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarViaje.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("INSERT INTO viajes_pendientes VALUES('0' , "
                        + "'"+jComboBoxCliente.getSelectedItem().toString()+"' , "
                        + "'"+jLabelIdCliente.getText()+"' , "
                        + "'"+jTextFieldPrecio.getText()+"' , "
                        + "'"+jTextFieldPrecio.getText()+"' , "
                        + "'"+jTextFieldFecha.getText()+"' ,"
                        + "'"+jTextFieldDescripcion.getText()+"')");
                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarViaje.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        
            
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            
            modelo.setRowCount(0);
            cargarViajes();

            jTextFieldPrecio.setText("");

        }
    }//GEN-LAST:event_jButtonListoActionPerformed

