    private void jButtonListo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListo1ActionPerformed
        if(jLabelId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No seleccionó el viaje a eliminar");
            
        }
       
        
        else{
            
            int resp = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar el viaje?", "Alerta!", JOptionPane.YES_NO_OPTION);
            if(resp==0){
               try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("DELETE FROM viajes WHERE id ='"+jLabelId.getText()+"'");
                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarViaje.class.getName()).log(Level.SEVERE, null, ex);
                }          
                
                
            
                modelo.setRowCount(0);
                cargarViajes();

                jTextFieldPrecio.setText("");
                JOptionPane.showMessageDialog(null, "Se ha eliminado el viaje");
            }
                

        }
    }//GEN-LAST:event_jButtonListo1ActionPerformed

